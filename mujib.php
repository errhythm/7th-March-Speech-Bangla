ইয়াহিয়া খান বলেছেন, আমি নাকি ১০ই মার্চ তারিখে গোলটেবিল বৈঠকে যোগদান করতে চেয়েছি, তাঁর সাথে টেলিফোন আমার আলাপ হয়েছে। 
আমি তাঁকে বলেছি আপনি দেশের প্রেসিডেণ্ট, ঢাকায় আসুন দেখুন আমার গরীব জনসাধারণকে কি ভাবে হত্যা করা হয়েছে, আমার মায়ের কোল খালি করা হয়েছে ।
আমি আগেই বলে দিয়েছি কোন গোলটেবিল বৈঠক হবে না। কিসের গোলটেবিল বৈঠক? কার গোলটেবিল বৈঠক? 
যারা আমার মা বোনের কোল শূন্য করেছে তাদের সাথে বসবো আমি গোলটেবিল বৈঠকে ?
তেসরা তারিখে পল্টনে আমি অসহযোগের আহবান জানালাম। 
বললাম, অফিস-আদালত, খাজনা-ট্যাক্স বন্ধ করুন।আপনারা মেনে নিলেন।
হঠাৎ আমার সঙ্গে বা আমাদের সঙ্গে আলোচনা না করে একজনের সঙ্গে পাঁচ ঘণ্টা বৈঠকের পর ইয়াহিয়া খান যে বক্তৃতা করেছেন, তাতে সমস্ত দোষ আমার ও বাংলার মানুষের উপর চাপিয়ে দিয়েছেন। 
দোষ করলেন ভুট্টো- কিন্তু গুলী করে মারা হলো আমার বাংলার মানুষকে। আমরা গুলী খাই, দোষ আমাদের- আমরা বুলেট খাই, দোষ আমাদের।
ইয়াহিয়া সাহেব অধিবেশন ডেকেছেন। কিন্ত আমার দাবী সামরিক আইন প্রত্যাহার করতে হবে, সেনাবাহিনীকে ব্যারাকে ফিরিয়ে নিতে হবে, হত্যার তদন্ত করতে হবে। 
তারপর বিবেচনা করে দেখবো পরিষদে বসবো কি বসনো না। এ দাবী মানার আগে পরিষদে বসার কোন প্রশ্নই ওঠে না, জনগণ আমাকে সে অধিকার দেয়নি।
রক্তের দাগ এখনো শুকায়নি, শহীদদের রক্ত মাড়িয়ে ২৫ তারিখে পরিষদে যোগ দিতে যাব না।
ভাইয়েরা, আমার উপর বিশ্বাস আছে? আমি প্রধানমন্ত্রীত্ব চাইনা, মানুষের অধিকার চাই। 
প্রধান মন্ত্রীত্বের লোভ দেখিয়ে আমাকে নিতে পারেনি, ফাঁসীর কাষ্ঠে ঝুলিয়ে নিতে পারেনি। 
আপনারা রক্ত দিয়ে আমাকে ষড়যন্ত্র মামলা থেকে মুক্ত করে এনেছিলেন। 
সেদিন এই রেসকোর্সে আমি বলেছিলাম, রক্তের ঋণ আমি রক্ত দিয়ে শোধ করবো; মনে আছে? 
আজো আমি রক্ত দিয়েই রক্তের ঋণ শোধ করতে প্রস্তুত।
আমি বলে দিতে চাই, আজ থেকে কোর্ট-কাচারী, হাইকোর্ট, সুপ্রীম কোর্ট, অফিস, আদালত, শিক্ষা প্রতিষ্ঠানসমুহ অনির্দিষ্ট-কালের জন্য বন্ধ থাকবে। 
কোন কর্মচারী অফিস যাবেন না। এ আমার নির্দেশ।
গরীবের যাতে কষ্ট না হয় তার জন্য রিক্সা চলবে, ট্রেন চলবে আর সব চলবে।
ট্রেন চলবে- তবে সেনাবাহিনী আনা-নেয়া করা যাবে না। করলে যদি কোন দূর্ঘটনা ঘটে তার জন্য আমি দায়ী থাকবো না।
সেক্রেটারীয়েট, সুপ্রীম কোর্ট, হাইকোর্ট জজকোর্ট সহ সরকারী, আধা-সরকারী এবং স্বায়ত্তশাসিত সংস্থাগুলো বন্ধ থাকবে। 
শুধু পূর্ব বাংলার আদান-প্রদানের ব্যাঙ্কগুলো দু-ঘন্টার জন্য খোলা থাকবে। পূর্ব বাংলা থেকে পশ্চিম পাকিস্তানে টাকা যেতে পারবেন না। 
টেলিগ্রাফ, টেলিফোন বাংলাদেশের মধ্যে চালু থাকবে। তবে, সাংবাদিকরা বহির্বিশ্বে সংবাদ পাঠাতে পারবেন।
এদেশের মানুষকে খতম করা হচ্ছে, বুঝে শুনে চলবেন। দরকার হলে সমস্ত চাকা বন্ধ করে দেয়া হবে।
আপনারা নির্ধারিত সময়ে বেতন নিয়ে আসবেন। যদি একটিও গুলী চলে তাহলে বাংলার ঘরে ঘরে দূর্গ গড়ে তুলবেন। 
যার যা আছে তাই নিয়ে শত্রুর মোকাবেলা করতে হবে। রাস্তা ঘাট বন্ধ করে দিতে হবে। আমরা তাদের ভাতে মারবো-পানিতে মারবো। 
হুকুম দিবার জন্য আমি যদি না থাকি, আমার সহকর্মীরা যদি না থাকেন, আপনারা আন্দোলন চালিয়ে যাবেন।
তোমরা আমার ভাই, তোমরা ব্যারাকে থাকো, কেউ কিছু বলবেনা। গুলী চালালে আর ভাল হবে না। 
সাত কোটি মানুষকে আর দাবীয়ে রাখতে পারবা না। বাঙ্গালী মরতেশিখেছে, তাদের কেউ দাবাতে পারবে না।
শহীদদের ও আহতদের পরিবারের জন্য আওয়ামী লীগ সাহায্যে কমিটি করেছে। আমরা সাহায্যের চেষ্টা করবো। আপনারা যে যা পারেন দিয়ে যাবেন।
সাত দিনের হরতালে যে সব শ্রমিক অংশ গ্রহণ করেছেন, কারফিউর জন্য কাজ করতে পারেননি-শিল্প মালিকরা তাদের পুরো বেতন দিয়ে দেবেন।
সরকারী কর্মচারীদের বলি, আমি যা বলি তা মানতে হবে। কাউকে যেন অফিসে দেখা না যায়। 
এ দেশের মুক্তি না হওয়া পর্যন্ত খাজনা-ট্যাক্স বন্ধ থাকবে। আপনারা আমার উপর ছেড়ে দেন, আন্দোলন কিভাবে করতে হয় আমি জানি।
কিন্তু হুঁশিয়ার, একটা কথা মনে রাখবেন, আমাদের মধ্যে শত্রু ঢুকেছে, ছদ্মবেশে তারা আত্মকহলের সৃষ্টি করতে চায়। 
বাঙ্গালী-অবাঙ্গালী, হিন্দু-মুসলমান সবাই আমাদের ভাই, তাদের রক্ষা করার দায়িত্ব আমাদের।
রেডিও, টেলিভিশন ও সংবাদপত্র যদি আমাদের আন্দোলনের খবর প্রচার না করে তবে কোন বাঙ্গালী রেডিও এবং টেলিভিশনে যাবেন না।
শান্তিপূর্ণভাবে ফয়সালা করতে পারলে ভাই ভাই হিসাবে বাস করার সম্ভাবনা আছে, তা না হলে নেই। বাড়াবাড়ি করবেন না, মুখ দেখাদেখিও বন্ধ হয়ে যেতে পারে।
প্রস্তুত থাকবেন, ঠাণ্ডা হলে চলবে না। আন্দোলন ও বিক্ষোভ চালিয়ে যাবেন। 
আন্দোলন ঝিমিয়ে পড়লে তারা আমাদের উপর ঝাঁপিয়ে পড়বে। শৃংখলা বজায় রাখুন। শৃংখলা ছাড়া কোন জাতি সংগ্রামে জয়লাভ করতে পারে না।
আমার অনুরোধ প্রত্যেক গ্রামে, মহল্লায়, ইউনিয়নে, আওয়ামী লীগের নেতৃত্বে সংগ্রাম কমিটি গড়ে তুলুন। হাতে যা আছে তাই নিয়ে প্রস্তুত থাকুন। 
রক্ত যখন দিয়েছি, রক্ত আরও দেবো। এদেশের মানুষকে মুক্ত করে ছাড়বো ইনশাল্লাহ।
এবারের সংগ্রাম, মুক্তির সংগ্রাম, এবারের সংগ্রাম, স্বাধীনতার সংগ্রাম।
জয় বাংলা। ";

	// Here we split it into lines.
	$speech = explode( "\n", $speech );

	// And then randomly choose a line.
	return wptexturize( $speech[ mt_rand( 0, count( $speech ) - 1 ) ] );
}

// This just echoes the chosen line, we'll position it later.
function Bangabandhu() {
	$chosen = Bangabandhu_get_7th_March_speech();
	$lang   = '';
	if ( 'en_' !== substr( get_user_locale(), 0, 3 ) ) {
		$lang = ' lang="en"';
	}

	printf(
		'<p id="Mujib"><span class="screen-reader-text">%s </span><span dir="ltr"%s>%s</span></p>',
		__( 'Quote from 7 March Speech of Bangabandhu:', 'Bangabandhu' ),
		$lang,
		$chosen
	);
}

// Now we set that function up to execute when the admin_notices action is called.
add_action( 'admin_notices', 'Bangabandhu' );

