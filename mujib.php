ভুট্টো সাহেব বলে গেছেন আলোচনার দরজা বন্ধ নয়; আরো আলোচনা হবে। 
মওলানা নুরানী ও মুফতি মাহুমুদ সহ পশ্চিম পাকিস্তানের অন্যান্য পার্লামেন্টারী নেতা এলেন, তাদের সঙ্গে আলোচনা হলো- উদ্দেশ্য ছিলো আলাপ-আলোচনা করে শাসনতন্ত্র রচনা করবো। 
তবে তাদের আমি জানিয়ে দিয়েছি ৬-দফা পরিবর্তনের কোন অধিকার আমার নেই, এটা জনগণের সম্পদ।
কিন্তু ভুট্টো হুমকি দিলেন। তিনি বললেন, এখানে এসে 'ডবল জিম্মী' হতে পারবেন না। পরিষদ কসাই খানায় পরিণত হবে। 
তিনি পশ্চিম পাকিস্তানী সদস্যদের প্রতি হুমকি দিলেন যে, পরিষদের অধিবেশনে যোগ দিলে রক্তপাত করা হবে, তাদের মাথা ভেঙে দেয়া হবে। হত্যা করা হবে। 
